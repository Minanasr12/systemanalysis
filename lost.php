                <h1>SYSTEM ANALYSIS :</h1>
            <p> the process by which an individual (s) studies a system such that an information system can be analyzed, modeled, and a logical alternative can be chosen. Systems analysis projects are initiated for three reasons: problems, opportunities, and directives
              <br>  
              is a process that many companies use to evaluate particular business situations and develop ways to improve them through more optimal methods. Companies may use this process to reshape their organization or meet business objectives related to growth and profitability
  <br>
  it provides an avenue for solutions in the system through the various tasks involved in doing the analysis. Through these various tasks, the overall quality of a system can be easily modified or improved and occurrences of errors can ultimately be reduced
    <br>
 system analysis is used in every field where something is developed. Analysis can also be a series of components that perform organic functions together, such as systems engineering
                
                
                
                </p>


